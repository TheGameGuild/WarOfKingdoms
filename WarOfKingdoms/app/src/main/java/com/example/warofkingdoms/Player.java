package com.example.warofkingdoms;

public class Player {
    private int life;
    private int energy;

    public Player(int life, int energy) {
        this.life = life;
        this.energy = energy;
    }

    public int getLife() {
        return life;
    }

    public void setLife(int life) {
        this.life = life;
    }

    public int getEnergy() {
        return energy;
    }

    public void setEnergy(int energy) {
        this.energy = energy;
    }
}






